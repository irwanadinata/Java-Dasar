public class TipeData {
    public static void main(String[] args) {

        //TIPE DATA PRIMITIF
        //-diawali dengan huruf kecil
        //-defaultnya adalah 0

        byte tipe1 = 1;
        short tipe2 = 2;
        int tipe3 = 3;
        long tipe4 = 4L;
        float tipe5 = 0;
        double tipe6 = 6.1;
        char tipe7 = 'a';
        boolean tipe8 = true;

        System.out.println(tipe1);
        System.out.println(tipe2);
        System.out.println(tipe3);
        System.out.println(tipe4);
        System.out.println(tipe5);
        System.out.println(tipe6);
        System.out.println(tipe7);
        System.out.println(tipe8);

        System.out.println("\n");

        //TIPE DATA BUKAN PRIMITIF/OBJEK
        //-diawali dengan huruf besar
        //-defaultnya null/kosong

        Byte tipe9 = 1;
        Short tipe10 = 2;
        Integer tipe11 = 3;
        Long tipe12 = 4L;
        Float tipe13 = null;
        Double tipe14 = 6.1;
        Character tipe15 = 'a';
        Boolean tipe16 = true;
        String tipe17 = "apa kabar";


        System.out.println(tipe9);
        System.out.println(tipe10);
        System.out.println(tipe11);
        System.out.println(tipe12);
        System.out.println(tipe13);
        System.out.println(tipe14);
        System.out.println(tipe15);
        System.out.println(tipe16);
        System.out.println(tipe17);
    }
}
