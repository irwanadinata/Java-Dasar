public class OperatorPerbandingan {
    public static void main(String[] args) {
        // <=, >=, !=, <, >, ==

        int nilai1 = 20;
        int nilai2 = 30;

        System.out.println(nilai1 > nilai2);
        System.out.println(nilai1 < nilai2);
        System.out.println(nilai1 >= nilai2);
        System.out.println(nilai1 <= nilai2);
        System.out.println(nilai1 != nilai2);
        System.out.println(nilai1 == nilai2);
    }
}
